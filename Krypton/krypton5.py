def getIOC(text): 
    '''
    CODE FROM
    https://gist.github.com/Will-Banksy/f4a5ea554b8e90e2c458c4106533f829
    '''
    letterCounts = []

    # Loop through each letter in the alphabet - count number of times it appears
    for i in range(26):
        count = 0
        for j in text:
            if j == chr(ord('A') + i):
                count += 1
        letterCounts.append(count)

    # Loop through all letter counts, applying the calculation (the sigma part)
    total = 0
    for i in range(len(letterCounts)):
        ni = letterCounts[i]
        total += ni * (ni - 1)

    N = len(text)
    c = 26.0 # Number of letters in the alphabet
    total = float(total) / ((N * (N - 1)))
    return total


def AverageIC(ct, i):
    '''
    Returns the average of the indexes of coincidence of a particular Vigenere ciphertext if the key is length i
    '''
    buckets = [""] * i
    for j in range(len(ct)):
        buckets[j % i] += ct[j]
    return sum([getIOC(bucket) for bucket in buckets]) / i

